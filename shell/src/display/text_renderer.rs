impl TextRenderer {

    // ----------------------------------------------------------
    // Main public API
    // ----------------------------------------------------------

    /// Draw a string at pixel position (x, y).
    /// `color` is [R, G, B] — alpha is always 255.
    /// Returns the pixel x position after the last character
    /// (useful for chaining or measuring text width).
    pub fn draw(
        frame:  &mut [u8],
        x:      usize,
        y:      usize,
        text:   &str,
        color:  [u8; 3],
    ) -> usize {
        let mut cx = x;
        for ch in text.chars() {
            let code = ch as u8;
            if code < 32 || code > 126 {
                cx += GLYPH_STRIDE;
                continue;
            }
            let idx = (code - 32) as usize;
            if idx < FONT.len() {
                Self::draw_glyph(frame, cx, y, &FONT[idx].0, color);
            }
            cx += GLYPH_STRIDE;
        }
        cx
    }

    /// Draw text centred horizontally within a region [cx_left, cx_right].
    pub fn draw_centered(
        frame:    &mut [u8],
        cx_left:  usize,
        cx_right: usize,
        y:        usize,
        text:     &str,
        color:    [u8; 3],
    ) {
        let text_w = Self::measure(text);
        let region_w = cx_right.saturating_sub(cx_left);
        let x = cx_left + region_w.saturating_sub(text_w) / 2;
        Self::draw(frame, x, y, text, color);
    }

    /// Measure pixel width of a string without drawing it.
    pub fn measure(text: &str) -> usize {
        text.chars()
            .filter(|c| *c as u32 >= 32 && *c as u32 <= 126)
            .count() * GLYPH_STRIDE
    }

    /// Truncate a string so it fits within `max_px` pixels,
    /// appending "…" (drawn as "..") if truncated.
    pub fn truncate(text: &str, max_px: usize) -> String {
        let ellipsis_w = GLYPH_STRIDE * 2; // ".."
        let mut out = String::new();
        let mut used = 0usize;
        let chars: Vec<char> = text.chars().collect();

        for (i, &ch) in chars.iter().enumerate() {
            let remaining = chars.len() - i;
            // If adding this char would overflow AND there are more chars after it
            if used + GLYPH_STRIDE > max_px.saturating_sub(if remaining > 1 { ellipsis_w } else { 0 }) {
                if remaining > 1 {
                    out.push_str("..");
                }
                break;
            }
            out.push(ch);
            used += GLYPH_STRIDE;
        }
        out
    }

    // ----------------------------------------------------------
    // Internal glyph painter
    // ----------------------------------------------------------
    fn draw_glyph(
        frame: &mut [u8],
        px:    usize,
        py:    usize,
        rows:  &[u8; 7],
        color: [u8; 3],
    ) {
        for (row, &mask) in rows.iter().enumerate() {
            for col in 0..GLYPH_W {
                if (mask >> (GLYPH_W - 1 - col)) & 1 == 1 {
                    let fx = px + col;
                    let fy = py + row;
                    if fx < SCREEN_WIDTH && fy < SCREEN_HEIGHT {
                        let i = (fy * SCREEN_WIDTH + fx) * 4;
                        frame[i]     = color[0];
                        frame[i + 1] = color[1];
                        frame[i + 2] = color[2];
                        frame[i + 3] = 255;
                    }
                }
            }
        }
    }
}